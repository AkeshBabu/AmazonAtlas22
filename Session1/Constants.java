
public class Constants {

	public static void main(String[] args) {
		
		/*
		 	This is to demonstrate a multi line comment
		 	In this program
		 	you will see
		 		- literal
		 		- final
		 */
		
		int salary = 50000; // 50000 is a literal i.e. data i.e. value i.e. constant
		salary = 75000; // salary is a variable but literal 75000 is a constant
		
		// final keyword in java makes the storage container read-only
		final int promoCode = 751209;
		//promoCode = 988981; // error : now promoCode cannot be modified :)

	}

}
